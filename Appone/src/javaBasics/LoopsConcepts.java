package javaBasics;

public class LoopsConcepts {
	
	
	public static void main (String args[]) {
		
		int i=0;//initialization
		while(i<=10) {//conditional
			System.out.println(i);
			i=i+1;  //increement/decreement
			
			
			
		}
		
		
		for(i=0;i<10;i++) {  //initialization,conditional,increement/decreement
			
			System.out.println(i);
			
		}
		System.out.println("*****************");
		
		//print 10 to 1
		
		for(i=10;i>0;i--) {
			
			System.out.println(i);
			
			
		}
		
	}

}
