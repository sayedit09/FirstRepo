package javaPractisecoding;

public class CountRepeatedWords {
	
	
	
	public static void main(String[] args) {
		
		String s1="pankaj";
		int count=0;
		
		for(int i=0;i<s1.length();i++) {
			if(s1.charAt(i)=='a') {
			count=count+1;
			}
			
			
		
		
	}
		System.out.println(count);

	}
}
